// Variables
// var - 더이상 안씀
// let const 

var name = 'Yu';
console.log(name);

let ive = 'IVE';
console.log(ive);
// let and var 로 선언하면 값을 추후 변경가능

const notchange = 'nono';
console.log(notchange);

